    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("yang dipilih : " + filePilihan.getAbsolutePath());

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                Class.forName(driver);
                conn = DriverManager.getConnection(koneksi, user, password);
                String baris;
                String pemisah = ",";

                while ((baris = br.readLine()) != null) {
                    String[] data = baris.split(pemisah);
                    String Id_Pasien = data[0];
                    String Nama_Pasien = data[1];
                    String Tanggal_Lahir = data[2];
                    String Alamat = data[3];
                    String No_Hp = data [4];
                    String Diagnosa = data [5];

                    if (!Id_Pasien.isEmpty() && !Nama_Pasien.isEmpty() && !Tanggal_Lahir.isEmpty() && !Alamat.isEmpty() && !No_Hp.isEmpty() && !Diagnosa.isEmpty()) {
                        String sql = "INSERT INTO data_pasien (id_pasien, nama_pasien, tanggal_lahir, alamat, no_hp, diagnosa) VALUES (?,?,?,?,?,?)";
                        pstmt = conn.prepareStatement(sql);
                        pstmt.setString(1, Id_Pasien);
                        pstmt.setString(2, Nama_Pasien);
                        java.sql.Date sqlDate = null;
                        try {
                        // Jika CSV berformat "dd/MM/yyyy" atau "dd-MM-yyyy"
                            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy"); // ubah sesuai format file kamu
                            java.util.Date parsedDate = sdf.parse(Tanggal_Lahir);
                            sqlDate = new java.sql.Date(parsedDate.getTime());
                        } catch (Exception e) {
                            System.out.println("⚠️ Format tanggal tidak valid: " + Tanggal_Lahir);
                            continue; // skip baris ini biar program tidak error
                        }
                        pstmt.setDate(3, sqlDate);
                        pstmt.setString(4, Alamat);
                        pstmt.setLong(5, Long.parseLong(No_Hp));
                        pstmt.setString(6, Diagnosa);

                        pstmt.executeUpdate();
                        
                        
                    } 
                }
                JOptionPane.showMessageDialog(null, "Sukses diinput");
                showTable();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(DataPasien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Gagal diinput");
            } catch (IOException ex) {
                Logger.getLogger(DataPasien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Gagal diinput");
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(DataPasien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnUploadActionPerformed

